public class _03_Half_Pyramid_Pattern {

    public static void main(String[] args) {
        
        int tl = 4;
        for(int line = 1; line<=tl; line++){
            for(int num = 1; num<=line; num++){
                System.out.print(num);
            }
            System.out.println();
        }


    }
    
}
