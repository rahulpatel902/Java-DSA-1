import java.util.Scanner;

public class _13_Type_Conversion {

    public static void main(String[] args) {
        
        
        /*
         * Type Conversion or Widening Conversion or Implicit Conversion 
        * It means => to covert one type of data to another type of data 
         * Number can only be converted into other numbers
         * same goes for word or letters
         * Conversion Happens when:
         * a. Type Compatible
         * b. Destination type should be greater then Source type
         * Byte -> short -> int -> float -> long -> double
         * int number can be converted into float
         * but float can not be converted into int
         */

        // Example

        Scanner sc = new Scanner(System.in);
        float num2 = sc.nextInt();
        System.out.println(num2);

        sc.close();





  

    }
    
}
