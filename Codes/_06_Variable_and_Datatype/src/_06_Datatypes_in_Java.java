public class _06_Datatypes_in_Java {

    public static void main(String[] args) {
        
         byte a = 8  ;        // 1 byte - Stores only 256 numbers
         short b = 250;     // 2 byte 
         char c = 'a';      // 2 byte - [a-z,A-Z,"#" "@","&"]
         boolean d = true;  // 1 byte - [true,false]
         int e = 25;        // 4 byte - -2Billion to 2Billion
         long f = 21554l;   // 8 byte - 
         float g = 10.5f;   // 4 byte - Decimal
         double h = 12.5;   // 8 byte - decimal
 
         System.out.println(a);
         System.out.println(b);
         System.out.println(d);
         System.out.println(c);
         System.out.println(e);
         System.out.println(f);
         System.out.println(g);
         System.out.println(h);


     
        

    }
    
}
